# Python 字串索引

credit_number = "1234-5678-9876-5432"

first_char = credit_number[0]
print(f"第一個字元為 {first_char}")

second_char = credit_number[1]
print(f"第二個字元為 {second_char}")

first_four_char = credit_number[0:4]
print(f"第1~4位字元為 {first_four_char}")

last_one = credit_number[-1]
print(f"最後1個字元為 {last_one}")

last_two = credit_number[-2]
print(f"最後2個字元為 {last_two}")
