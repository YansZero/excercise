# 變數是一個可以重複使用的容器,用於儲存值,變數的行為就好像它包括的質一樣。
# 在寫程式中也是一樣的。
# 當我們創建一個變數時,我們需要給它一個獨特的名稱,來表示所包含的內容。

# integer 整數
x = 5
y = 6
print(f"整數x+y的值是: {x + y}")
print(f"x,y 的type是: {type(x), type(y)}")

# float 浮點數
z = 3.1415
print(f"浮點數z的值是: {z}")
print(f"z 的type是: {type(z)}")

# string 字串
name = 'Jason'
print(f"字串name的值是: {name}")
print(f"name 的type是: {type(name)}")

# boolean #true # false
is_online = False
print(f"你在線上嗎? {is_online}")
print(f"is_online 的type是: {type(is_online)}")

# ex:
a = 1
b = 2.2
c = '隨便拉'
d = True
print(f"a,b,c,d值為: {a,b,c,d}")
